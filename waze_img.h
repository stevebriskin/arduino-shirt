unsigned long lastchange;
int pic = 0;

void setupWazePic() {

}

void loopWazePic(CRGB* leds, int num) {
  unsigned long now = millis();

  if (lastchange == 0 || now - lastchange > 3000ul) {
    pic = abs(pic - 1);
    lastchange = now;
  }
 
    if (pic == 0) {
      for( int i = 0; i < num; i++) {
        leds[i] = pgm_read_dword(&(waze[i]));
      }
    }
    else if (pic == 1) {
      for( int i = 0; i < num; i++) {
        leds[i] = pgm_read_dword(&(ads[i]));
      }
    }
}
